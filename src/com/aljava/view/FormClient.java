    private void buttonSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonSalvarMouseClicked
        /*CriaÃ§Ã£o do objeto cliente*/
        Client cliente = new Client(
                textNome.getText(),
                textCPF.getText(),
                textRG.getText(),
                textEmail.getText(),
                textTelefone.getText(),
                textEndereco.getText(),
                textCidade.getSelectedItem().toString(),
                textUf.getSelectedItem().toString(),
                textBairro.getText(),
                Integer.parseInt(textNumero.getText())
        );

        /*CriaÃ§Ã£o do objeto DAO para fazer a inserÃ§Ã£o dos dados no banco.*/
        DAO<Client> dao = new DAO<>(Client.class);
        dao.abrirT().incluir(cliente).fecharT().fechar();/*Sequencia de metodos onde faz abertura da transiÃ§Ã£o, inclui o cliente, depois fecha a transiÃ§Ã£o e por fim fecha a entityManage*/
        
        JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso!!");
        new Utilitarios().limpaTela(panelDadosPessoais);
    }//GEN-LAST:event_buttonSalvarMouseClicked

